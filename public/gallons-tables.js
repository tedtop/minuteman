function inchesToGallons(tankId, inches) {
    const table = tankId === 'T7' ? T7_GALLONS
        : tankId === 'LF' ? LF_GALLONS
        : T1_6_GALLONS;

    const maxIndex = table.length - 1;
    const clamped = Math.max(0, Math.min(inches, maxIndex));

    const lo = Math.floor(clamped);
    const hi = Math.ceil(clamped);
    if (lo === hi) return table[lo];

    return Math.round(table[lo] + (table[hi] - table[lo]) * (clamped - lo));
}
